package jsoftware.com.jutil.util;

/**
 *
 * @author juan-campos
 */
public interface JArrays {

    public static <T> T[] array(T... e) {
        return (T[]) e;
    }

    public static int[] array(int... e) {
        return e;
    }

    public static char[] array(char... e) {
        return e;
    }

    public static boolean[] array(boolean... e) {
        return e;
    }

    public static long[] array(long... e) {
        return e;
    }

    public static double[] array(double... e) {
        return e;
    }

    public static float[] array(float... e) {
        return e;
    }

    public static short[] array(short... e) {
        return e;
    }

    public static byte[] array(byte... e) {
        return e;
    }
}
